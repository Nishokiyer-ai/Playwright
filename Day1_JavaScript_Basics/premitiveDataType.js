// Premitive Data types in JavaScript

var firstName = 'John'; // String
console.log(firstName);
console.log(typeof firstName);

var companyName = "Manaran Company"; // String
console.log(companyName);
console.log(typeof companyName);

var mobileNumber = 9876543210; // Number
console.log(mobileNumber);
console.log(typeof mobileNumber);

var isAutomation = true; // Boolean
console.log(isAutomation);
console.log(typeof isAutomation);

var hasPlaywright
console.log(typeof hasPlaywright); // Undefined
console.log(hasPlaywright);

var accountNumber = 45435435345345433453n;
console.log(accountNumber); // BigInt
console.log(typeof accountNumber);

var abc = null; // Null
console.log(abc);
console.log(typeof abc); // Note: typeof null returns 'object' due to a historical bug in JavaScript

var prism = `This is a symbol`;
console.log(prism); // Symbol
console.log(typeof prism);

